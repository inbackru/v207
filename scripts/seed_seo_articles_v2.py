def get_or_create_category(name):
    from models import Category, Admin
    from app import db

    SLUG_MAP = {
        "Ипотека": "ipoteka",
        "Советы покупателям": "sovety-pokupatelyam",
        "Инвестиции": "investicii",
        "Районы Краснодара": "rajony-krasnodara",
        "Инструкции": "instrukcii",
        "Интересное": "interesnoe",
        "Новости": "novosti",
    }
    slug = SLUG_MAP.get(name, name.lower().replace(" ", "-"))
    cat = Category.query.filter_by(name=name).first()
    if not cat:
        cat = Category(name=name, slug=slug, is_active=True)
        db.session.add(cat)
        db.session.flush()
    return cat


def run():
    with app.app_context():
        from models import BlogPost, Category, Admin
        from app import db

        admin = Admin.query.first()
        if not admin:
            print("❌ No admin user found. Run the app first to create an admin.")
            return

        created = 0
        skipped = 0
        base_date = datetime(2024, 6, 1)

        for i, art in enumerate(ALL_ARTICLES):
            # Check if already exists (by title)
            existing = BlogPost.query.filter_by(title=art['title']).first()
            if existing:
                skipped += 1
                continue

            cat = get_or_create_category(art['cat'])

            # Generate slug
            from utils.transliteration import create_slug
            base_slug = create_slug(art['title'])
            slug = base_slug
            counter = 1
            while BlogPost.query.filter_by(slug=slug).first():
                slug = f"{base_slug}-{counter}"
                counter += 1

            pub_date = base_date + timedelta(days=i * 2)

            post = BlogPost(
                title=art['title'],
                slug=slug,
                content=art['content'],
                excerpt=art['excerpt'],
                meta_title=art['title'] + ' | InBack',
                meta_description=art['excerpt'],
                meta_keywords=art.get('keywords', ''),
                status='published',
                published_at=pub_date,
                created_at=pub_date,
                updated_at=pub_date,
                author_id=admin.id,
                category_id=cat.id,
                views_count=random.randint(50, 2500),
            )
            db.session.add(post)
            db.session.flush()

            # Set reading_time if column exists
            try:
                post.reading_time = art.get('reading_time', 5)
            except Exception:
                pass

            created += 1

        db.session.commit()
        print(f"✅ Done: {created} created, {skipped} skipped (already exist).")


if __name__ == '__main__':
    run()
