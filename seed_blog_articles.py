def seed():
    with app.app_context():
        from models import BlogPost, Admin

        admin = Admin.query.first()
        if not admin:
            print("❌ Нет ни одного администратора в базе. Создайте admin-аккаунт и повторите.")
            sys.exit(1)

        created = 0
        skipped = 0
        for art in ARTICLES:
            existing = BlogPost.query.filter_by(slug=art["slug"]).first()
            if existing:
                skipped += 1
                continue

            pub_date = datetime.utcnow() - timedelta(days=art["days_ago"])
            post = BlogPost(
                title=art["title"],
                slug=art["slug"],
                excerpt=art["excerpt"],
                content=art["content"].strip(),
                meta_title=art["meta_title"],
                meta_description=art["meta_description"],
                status="published",
                author_id=admin.id,
                published_at=pub_date,
                created_at=pub_date,
                updated_at=pub_date,
                views_count=0,
                likes_count=0,
            )
            db.session.add(post)
            created += 1
            print(f"  ✅ {art['title'][:60]}")

        db.session.commit()
        print(f"\n✅ Готово: создано {created} статей, пропущено {skipped} (уже существуют).")


if __name__ == "__main__":
    seed()
